// 예제02-02 : var 키워드를 사용하여 변수 선언

package 예제0202;

public class Var {
	
	public static void main(String[] args) {
		var price = 200;
		var name = "yoomin";
		var pi = 3.14;
		
		System.out.println("price = "+(price+1000));
		System.out.println("name = "+name);
		System.out.println("pi = "+pi*10);
	}
}
